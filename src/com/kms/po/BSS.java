package com.kms.po;

public class BSS {
	private String bknm;
	private String sup;
	private String sid;
	private String uptm;
	private String prc;
	private String msg;
	public String getBknm() {
		return bknm;
	}
	public void setBknm(String bknm) {
		this.bknm = bknm;
	}
	public String getSup() {
		return sup;
	}
	public void setSup(String sup) {
		this.sup = sup;
	}
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getUptm() {
		return uptm;
	}
	public void setUptm(String uptm) {
		this.uptm = uptm;
	}
	public String getPrc() {
		return prc;
	}
	public void setPrc(String prc) {
		this.prc = prc;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}

}
